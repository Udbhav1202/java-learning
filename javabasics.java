public class javabasics {
    public static void main(String args[]) {
       System.out.print("hello world"); // semicolon is statement terminator
       System.out.print("hello world\n"); // backslash n next line  me jane ke liye hota hai n ke bad jo bhi hoga wo next line me chala jayega
       System.out.println("hello world"); // ln print ke bad line ka space deta hai ya next line pe le jata hai
       System.out.println("hello world"); // do fucn print ke hote hai ek sirf print aur ek next line me jane ke liye ln hota hai
       System.out.print("****\n***\n**\n*\n");
       System.out.println("****");
       System.out.println("***"); // quotation mark ke andar kuch bhi likha hoga wo as it is print ho jayega
       System.out.println("**");
       System.out.println("*");
       int a = 10; // java me variable pe value store karne ke liye shuruwat me variable pe uska type likhte hai jaise (int a = 10;)
       int b = 5;
       System.out.println(a); // kisi variable ki value print karane ke liye print wale funcn pe quotation mark nhi lagate
       System.out.println(b);
       System.out.println(a + b);
       String name = "udbhav srivastava";
       System.out.println(name);


       a = 100; //variable ki value change karne ke liye kuch derr code chalne ke bad a ko assign kiya nyi value ke sath so a ko print karane pe nyi value print ho gyi
       System.out.println(a);

       a = b; // a pe b ki value assign kar di 
       System.out.println(a);

    }
}